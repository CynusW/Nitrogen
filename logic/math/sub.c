#include <stdio.h>
#include <stdlib.h>


// subP - prints the subtraction
int subP(int num1, int num2){
  if (num1 > num2){
    return num1 - num2;
  }
  return num2 - num1;
}

// subR - returns the substraction
int subR(int num1, int num2){
  if (num1 > num2){
    return num1 - num2;
  }
  return num2 - num1;
}
